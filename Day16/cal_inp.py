# This is a simple calculator program that performs addition, 
# subtraction, and multiplication.
# # It defines three functions: add, subtract, and multiply, with input 
# parameters for the numbers to be operated on.
def add (a,b):
    addition = a + b
    return  addition
def subtract(a,b):
    sub = a - b
    return  sub
def multiply(a,b):
    mul = a * b
    return  mul
print ("Addition :", add(5,10))
print ( " Sub :", subtract(10,5))
print ( " Mul :", multiply(5,10))